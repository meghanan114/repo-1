
public class FirstProgram 
{
	public static void main(String args[]) 
	{
		for (int i = 1; i <= 1; i++) 
		{
			for (int j = 1; j <= 1; j++) 
			{
				char a=65;
				System.out.print(a);
			}
		}
		System.out.println();
		for (int i = 2; i <= 2; i++) 
		{
			for (int j = 3; j <= 3; j++) 
			{
				char a=65;
				char b=66;
				System.out.print(a);
				System.out.print(b);
			}
		}
		System.out.println();
		for (int i = 3; i <= 3; i++) 
		{
			for (int j = 3; j <= 3; j++) 
			{
				char a=65;
				char b=66;
				char c=67;
				System.out.print(a);
				System.out.print(b);
				System.out.print(c);
			}
		}
		System.out.println();
	}
}
